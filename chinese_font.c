// Binary search for GBK code in the lookup table
const uint8_t *CHINESE_GetGlyph(uint16_t gbk_code)
{
    int low = 0;
    int high = CHINESE_FONT_CHAR_COUNT - 1;

    while (low <= high) {
        int mid = (low + high) / 2;
        if (gChineseFontCodes[mid] == gbk_code)
            return gChineseFontData[mid];
        else if (gChineseFontCodes[mid] < gbk_code)
            low = mid + 1;
        else
            high = mid - 1;
    }

    return (void *)0; // not found
}

